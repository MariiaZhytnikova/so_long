#include "libft.h"

void	*ft_memchr(const void *ptr, int c, size_t n)
{
	size_t				i;
	const unsigned char	*str;

	str = (unsigned const char *)ptr;
	i = 0;
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return ((void *)(str + i));
		i++;
	}
	return (NULL);
}
